package e_oop;

public class Calculator {

	// 더하기 빼기 곱하기 나누기 나머지

	double sum(double a, double b){
		return a+b;
	}
	double sub(double a, double b){
		return a-b;
	}
	
	double mul(double a, double b){
		return a*b;
	}
	
	double div(double a, double b){
		return a/b;
	}
	
	double rem(double a, double b){
		return a%b;
	}
	
}
